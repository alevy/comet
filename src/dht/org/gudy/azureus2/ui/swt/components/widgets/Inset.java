package org.gudy.azureus2.ui.swt.components.widgets;

public class Inset
{
	public int top = 0;

	public int bottom = 0;

	public int left = 0;

	public int right = 0;

	public Inset(int left, int right, int top, int bottom) {
		this.left = left;
		this.right = right;
		this.top = top;
		this.bottom = bottom;

	}
}
